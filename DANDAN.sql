ID INT PRIMARY KEY AUTO_INCREMENT,
TRANSACTION_DATE_LOG DATETIME,
ID_CLIENTS INT,
DESCRIPTION VARCHAR(100),
INVESTMENT_VALUE DECIMAL(15,2),
FOREIGN KEY (ID_CLIENTS) REFERENCES CONT_CLIENTS(ID)
