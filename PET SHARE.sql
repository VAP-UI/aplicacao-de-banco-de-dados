DELIMITER ;






-- TRIGGER PRA QUANDO ADICIONAR CLIENTE --

DELIMITER $$

CREATE TRIGGER LOG_PS_NEW_CLIENT
AFTER INSERT ON PS_CLIENTS
FOR EACH ROW
BEGIN
    INSERT INTO PS_LOGS (TABLE_NAME, NEW_ID, DESCRIPTION, LOG_DATE)
    VALUES ('CLIENT', NEW.ID, 'NEW CLIENT', NOW());
END$$

DELIMITER ;


-- TRIGGER PRA QUANDO DELETAR CLIENTE --

DELIMITER $$

CREATE TRIGGER LOG_PS_DELETE_CLIENT
AFTER DELETE ON PS_CLIENTS
FOR EACH ROW
BEGIN
INSERT INTO PS_LOGS (TABLE_NAME, NEW_ID, DESCRIPTION, LOG_DATE)
VALUES ('CLIENT', OLD.ID, 'DELETED CLIENT', NOW());
END$$

DELIMITER ;



