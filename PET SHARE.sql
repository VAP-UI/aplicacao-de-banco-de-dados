-- PROCEDURE PARA NOVO CLIENTES --

DELIMITER $$

CREATE PROCEDURE NEW_PS_CLIENT (
IN P_NAME VARCHAR(100),
IN P_EMAIL VARCHAR(50),
IN P_PHONE VARCHAR(30),
IN P_TYPE VARCHAR(20)
)

BEGIN
	INSERT INTO PS_CLIENTS (NAME, EMAIL, PHONE, CLIENT_TYPE)
	VALUES (P_NAME, P_EMAIL, P_PHONE, P_TYPE);
END$$

DELIMITER ;

-- PROCEDURE PARA DELETAR CLIENTE

DELIMITER $$

CREATE PROCEDURE DELETE_PS_CLIENT (
    IN P_ID INT
)
BEGIN
    DELETE FROM PS_CLIENTS
    WHERE ID = P_ID;
END$$

DELIMITER ;

-- PROCEDURE PARA NOVO PET --


DELIMITER $$

CREATE PROCEDURE NEW_PS_PETS (
IN P_NAME VARCHAR(100),
IN P_AGE INT,
IN P_TYPE VARCHAR(50),
IN P_BREED VARCHAR(50),
IN P_STATUS VARCHAR(20)
)

BEGIN
	INSERT INTO PS_PETS (NAME, AGE, TYPE, BREED, STATUS)
	VALUES (P_NAME, P_AGE, P_TYPE, P_BREED, P_STATUS);
END$$

DELIMITER ;


-- PROCEDURE PARA DELETAR PET --

DELIMITER $$

CREATE PROCEDURE DELETE_PS_PETS (
    IN P_ID INT
)
BEGIN
    DELETE FROM PS_PETS
    WHERE ID = P_ID;
END$$

DELIMITER ;


-- PROCEDURE PARA ATUALIZAR PET --

DELIMITER $$

CREATE PROCEDURE UPDATE_PS_PETS (
IN P_ID INT,
IN P_NAME VARCHAR(100),
IN P_AGE INT,
IN P_TYPE VARCHAR(50),
IN P_BREED VARCHAR(50),
IN P_STATUS VARCHAR(20)
)

BEGIN
	UPDATE PS_PETS
	SET NAME = P_NAME,
	AGE = P_AGE,
	TYPE = P_TYPE,
	BREED = P_BREED,
	STATUS = P_STATUS
	WHERE ID = P_ID;
END$$

DELIMITER ;


