USE DB_T2_VITOR_PIRES

-- CRIAÇÃO DAS TABELAS --

CREATE TABLE PS_CLIENTS (
ID INT PRIMARY KEY AUTO_INCREMENT,
NAME VARCHAR(100),
EMAIL VARCHAR(100),
PHONE VARCHAR(30),
CLIENT_TYPE VARCHAR(20)
);


CREATE TABLE PS_PETS (
ID INT PRIMARY KEY AUTO_INCREMENT,
NAME VARCHAR(50),
AGE INT,
TYPE VARCHAR(50),
BREED VARCHAR(50),
STATUS VARCHAR(50) DEFAULT 'AVAILABLE'
);


CREATE TABLE PS_RENTALS (
ID INT AUTO_INCREMENT PRIMARY KEY,
CLIENTS_ID INT,
PETS_ID INT,
START_DATE TIMESTAMP,
END_DATE TIMESTAMP,
HOURS INT,
PRICE_PER_HOUR DECIMAL(10,2),
TOTAL_VALUE DECIMAL (10,2),
STATUS VARCHAR(15) DEFAULT 'ACTIVE',
FOREIGN KEY (CLIENTS_ID) REFERENCES PS_CLIENTS(ID),
FOREIGN KEY (PETS_ID) REFERENCES PS_PETS(ID)
);


CREATE TABLE PS_LOGS (
ID INT PRIMARY KEY AUTO_INCREMENT,
TABLE_NAME VARCHAR(100),
NEW_ID INT,
DESCRIPTION VARCHAR(100),
LOG_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


